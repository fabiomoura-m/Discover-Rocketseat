/*

  String

  * Cadeia de caracteres

  "" // aspas duplas
  '' // aspas simples
  `` // template literals ou template strings

*/

console.log('Fabio')
console.log("'Fabio'") // pode usar quantas aspas simples quiser
console.log('"Fabio"') // pode usar quantas aspas duplas quiser
console.log(`"fabio"'Moura'`) // posso usar aspas simples e duplas
console.log(`
"fabio"
Moura
`) // permite quebrar linhas tbm (multilinhas)
console.log(`Fabio ${1 + 1}`) // permite colocar expressões de linguagem (interpolação)
