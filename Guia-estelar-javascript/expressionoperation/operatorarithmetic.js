/*
  Operadores Aritméticos
*/
// Multiplicação *
console.log(5.4 * 2)

// Divisão /
console.log(12 / 2)

// Soma +
console.log(5 + 5)

// Subtração -
console.log(34 - 23)

// Resto da divisão %
console.log(3 % 7)
console.log(11 % 4)

// Incremento ++
let increment = 0
increment++
console.log(increment)
